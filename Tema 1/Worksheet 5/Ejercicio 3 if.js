age = parseInt(prompt("Introduce tu edad"));

if (age <= 5){
    alert("El usuario pertenece al jardín de infancia");
}
if (age >=6 && age <=11){
    alert("El usuario pertenece a la educación primaria");
}
if (age >=12 && age <=16){
    alert("El usuario pertenece a la ESO")
}
if (age >=17 && age <=21){
    alert("El usuario pertenece a Bachillerato o a los Ciclos Formativos")
}
if (age >= 22){
    alert("El usuario pertenece a la Universidad")
}